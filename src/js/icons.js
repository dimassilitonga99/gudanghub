/**
 * Auto-render semua icon dari data-icon attribute
 */
export function injectIcons(container = document) {
  container.querySelectorAll('[data-icon]').forEach((el) => {
    // Skip kalau sudah ada SVG di dalam
    if (el.querySelector('svg')) return;

    const name = el.dataset.icon;
    const size = parseInt(el.dataset.iconSize, 10) || 20;
    const color = el.dataset.iconColor || 'currentColor';
    const strokeWidth = parseFloat(el.dataset.iconStroke) || 2;

    el.innerHTML = icon(name, { size, color, strokeWidth });
  });
}

