/**
 * Render icon sebagai HTML SVG string
 */
export function icon(name, options = {}) {
  const {
    size = 20,
    color = 'currentColor',
    strokeWidth = 2,
    className = '',
  } = options;

  const pathData = ICON_PATHS[name];

  if (!pathData) {
    console.warn(`[Icons] Icon "${name}" tidak ditemukan.`);
    return `<span style="display:inline-block;width:${size}px;height:${size}px;background:#ff6b0033;border-radius:4px;" title="Icon not found: ${name}"></span>`;
  }

  return `<svg
    xmlns="http://www.w3.org/2000/svg"
    width="${size}"
    height="${size}"
    viewBox="0 0 24 24"
    fill="none"
    stroke="${color}"
    stroke-width="${strokeWidth}"
    stroke-linecap="round"
    stroke-linejoin="round"
    class="lucide-icon ${className}".trim()
  >${pathData}</svg>`;
}

